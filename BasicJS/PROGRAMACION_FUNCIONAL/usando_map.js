/*
Programación funcional: utilice el método map para extraer datos de una matriz

Hasta ahora hemos aprendido a utilizar funciones puras para evitar efectos secundarios en un programa. Además, hemos visto que el valor de tener una función solo depende de sus argumentos de entrada.

Este es sólo el comienzo. Como sugiere su nombre, la programación funcional se centra en una teoría de funciones.

Tendría sentido poder pasarlos como argumentos a otras funciones y devolver una función de otra función. Las funciones se consideran objetos de primera clase en JavaScript, lo que significa que pueden usarse como cualquier otro objeto. Se pueden guardar en variables, almacenar en un objeto o pasar como argumentos de función.

Comencemos con algunas funciones simples de matriz, que son métodos del prototipo del objeto de matriz. En este ejercicio estamos mirando Array.prototype.map(), o más simplemente map.

El mapmétodo itera sobre cada elemento de una matriz y devuelve una nueva matriz que contiene los resultados de llamar a la función de devolución de llamada en cada elemento. Hace esto sin mutar la matriz original.

Cuando se usa la devolución de llamada, se le pasan tres argumentos. El primer argumento es el elemento actual que se está procesando. El segundo es el índice de ese elemento y el tercero es la matriz en la que mapse llamó al método.

*/


/*
Vea a continuación un ejemplo que utiliza el map método en la users matriz para devolver una nueva matriz
que contiene solo los nombres de los usuarios como elementos.
Por simplicidad, el ejemplo solo usa el primer argumento de la devolución de llamada.
*/
const users = [
    { name: 'John', age: 34 },
    { name: 'Amy', age: 20 },
    { name: 'camperCat', age: 10 }
];

const names = users.map(user => user.name);
console.log(names); // [ 'John', 'Amy', 'camperCat' ]


