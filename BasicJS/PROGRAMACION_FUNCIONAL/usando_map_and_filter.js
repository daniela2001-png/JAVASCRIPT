/*

Programación funcional: use el método de filtro para extraer datos de una matriz

Otra función de matriz útil es Array.prototype.filter(), o simplemente filter().

filterllama a una función en cada elemento de una matriz y devuelve una nueva matriz que contiene solo los elementos para los que esa función devuelve true. En otras palabras, filtra la matriz en función de la función que se le pasa. Al igual que map, hace esto sin necesidad de modificar la matriz original.

La función de devolución de llamada acepta tres argumentos. El primer argumento es el elemento actual que se está procesando. El segundo es el índice de ese elemento y el tercero es la matriz en la que filterse llamó al método.

*/


/*

Vea a continuación un ejemplo que usa el filter método en la users matriz
para devolver una nueva matriz que contiene solo a los usuarios menores de 30 años.
Para simplificar, el ejemplo solo usa el primer argumento de la devolución de llamada.

*/

const users = [
    { name: 'John', age: 34 },
    { name: 'Amy', age: 20 },
    { name: 'camperCat', age: 10 }
];

const usersUnder30 = users.filter(user => user.age < 30);
console.log(usersUnder30); // [ { name: 'Amy', age: 20 }, { name: 'camperCat', age: 10 } ]


